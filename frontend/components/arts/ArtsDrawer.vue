<template>
  <div :class="`arts-right-panel`">
    <div class="panel-header">
      <div class="panel-close-btn cursor-pointer" @click="togglePanel">
        <span class="mr-2 font-weight-bold"> X </span>
        Close
      </div>
    </div>

    <!-- Render List of Artist -->
    <div v-if="this.$route.name !== 'index-art-art'">
      <div v-if="listOfArtists.length === 0" class="panel-artist">
        <img
          v-lazy="renderArtistImg(artDetails.image)"
          class="artist-img-small"
        />
        <div class="panel-details">
          <span class="item-title">{{ artDetails.name }}</span>
          <span class="item-subtitle">{{ artDetails.type }}</span>
          <div
            class="cursor-pointer pl-2 pr-2 profile-btn"
            @click="checkArtistProfile(artDetails.name)"
          >
            Visit Profile
          </div>
        </div>
      </div>
      <div
        v-for="artist in listOfArtists"
        v-else
        :key="artist.id"
        class="panel-artist"
      >
        <img v-lazy="renderArtistImg(artist.image)" class="artist-img-small" />
        <div class="panel-details">
          <span class="item-title">{{ artist.name }}</span>
          <div
            class="cursor-pointer pl-2 pr-2 profile-btn"
            @click="checkArtistProfile(artist.name)"
          >
            Visit Profile
          </div>
        </div>
      </div>
    </div>

    <b-row v-if="listOfPublicArt" class="ml-1 mr-1 media-list-container">
      <b-col
        v-for="(artwork, index) in listOfPublicArt"
        :key="artwork.id"
        lg="12"
        xl="12"
        md="6"
        sm="12"
        @click="showMedia(artwork, index)"
      >
        <MediaCard
          class="mt-3 hover-left-move"
          :media="artwork"
          :geometry="geometry"
          :type="'public_art'"
        >
        </MediaCard>
      </b-col>
    </b-row>

    <!-- Render List of Medias -->
    <b-row class="media-list-container">
      <b-col
        v-for="media in listOfMedias"
        :key="media.id"
        lg="12"
        xl="12"
        md="6"
        sm="12"
        @click="showMedia(media)"
      >
        <MediaCard
          class="mt-3 hover-left-move"
          :media="media"
          :geometry="geometry"
          :type="'media'"
        >
        </MediaCard>
      </b-col>
    </b-row>

    <!-- Render Gallery with Media Info -->
    <Gallery
      v-if="showGallery"
      :media="currentMedia"
      :artists="listOfArtists"
      :placename="artDetails.name"
      :art-thumbnail="renderArtistImg"
      :placename-img="artDetails.image"
      :toggle-gallery="toggleGallery"
      :check-profile="checkArtistProfile"
      :related-media="listOfImageMedia"
    />
  </div>
</template>

<script>
import MediaCard from '@/components/MediaCard.vue'
import { encodeFPCC } from '@/plugins/utils.js'
import Gallery from '@/components/Gallery.vue'

export default {
  components: {
    MediaCard,
    Gallery
  },
  props: {
    art: {
      type: Object,
      default: () => {
        return {}
      }
    },
    togglePanel: {
      type: Function,
      default: () => {
        return {}
      }
    }
  },
  data() {
    return {
      currentMedia: this.art.currentMedia
    }
  },
  computed: {
    showGallery() {
      // Similar to previous comment. This shouldn't sound like a function because this is a computed value -> Basically a variable.
      return this.$store.state.sidebar.showGallery
    },
    artDetails() {
      // Nice
      return this.art.art
    },
    listOfPublicArt() {
      // I'm not sure if I'm right on this one, but I would normally not use a computed value for another computed value.
      // It's not applicable here, but if you remove artDetails, you'd have to update all the other computed values dependent
      // on that one, and it causes unwarranted updates.
      return this.artDetails.public_arts || [] // Change this to this.art.art.public_arts -> to make it a stand-alone value
    },
    listOfArtists() {
      // Same comment above
      return this.artDetails.artists || [] // Change this to this.art.art.artists -> to make it a stand-alone value
    },
    listOfMedias() {
      // Nice
      // Could be shortened to mediaList though. I'm not sure about our conventions, but mediaList sounds better and it's shorter. Actually, same comment goes to the two items above.
      return this.artDetails.medias.filter(
        media => media.file_type !== 'default'
      )
    },
    listOfImageMedia() {
      // Same comment above. Could be renamed to mediaImageList
      return [
        ...this.listOfPublicArt,
        ...this.listOfMedias.filter(media => media.file_type === 'image')
      ]
    },
    geometry() {
      // This is my bad. We should have a more consistent data set. Will update this in the future.
      return this.artDetails.geometry || this.artDetails.geom
    }
  },
  mounted() {
    this.$store.commit('sidebar/setGallery', !!this.currentMedia)
  },
  methods: {
    toggleGallery() {
      // Nice. Worded perfectly.
      this.$store.commit('sidebar/setGallery', !this.showGallery)
    },
    showMedia(media) {
      // Nice. Function name sounds like an action as it should.
      this.toggleGallery()
      this.currentMedia = media
    },
    renderArtistImg(img) { // This isn't really rendering. Should be getArtistImage, I assume.
      return img || require(`@/assets/images/${this.artDetails.type}_icon.svg`)
    },
    checkArtistProfile(name) {
      // Nice
      this.toggleGallery()
      this.$router.push({
        path: `/art/${encodeFPCC(name)}`
      })
    }
  }
}
</script>

